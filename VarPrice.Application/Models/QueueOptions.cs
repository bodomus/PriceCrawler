namespace VarPrice.Application.Models;

public sealed class QueueOptions
{
    /// <summary>
    /// Максимальное количество элементов очереди, которое worker резервирует за один цикл обработки.
    /// Используется `RunCrawlerUseCase` при чтении pending/retry URL из очереди.
    /// Увеличение размера батча снижает число обращений к базе, но может дольше удерживать lease на большой группе URL.
    /// </summary>
    public int BatchSize { get; set; } = 50;

    /// <summary>
    /// Задержка между опросами очереди в миллисекундах, когда сейчас нет доступных элементов.
    /// Используется в цикле drain очереди, пока остаются pending или retry элементы.
    /// Меньшее значение быстрее подхватывает новые URL, большее снижает частоту запросов к базе.
    /// </summary>
    public int PollDelayMs { get; set; } = 300;

    /// <summary>
    /// Длительность lease для зарезервированного элемента очереди в секундах.
    /// Используется при резервировании URL, чтобы другой worker не взял тот же элемент до истечения lease.
    /// Значение должно покрывать типичное время обработки URL; слишком малое значение увеличивает риск повторной обработки.
    /// </summary>
    public int LeaseSeconds { get; set; } = 60;

    /// <summary>
    /// Максимальное количество попыток обработки одного URL до перевода в dead.
    /// Используется retry policy после ошибок извлечения или обработки карточки.
    /// Увеличение дает transient-ошибкам больше шансов восстановиться, но дольше держит проблемные URL в очереди.
    /// </summary>
    public int MaxAttempts { get; set; } = 3;

    /// <summary>
    /// Базовая задержка перед повторной обработкой URL в миллисекундах.
    /// Используется `QueueRetryPolicy` для расчета backoff после неудачной попытки.
    /// Увеличение уменьшает частоту повторов проблемных URL и снижает нагрузку на источник.
    /// </summary>
    public int RetryBaseDelayMs { get; set; } = 500;

    /// <summary>
    /// Максимальная задержка перед повторной обработкой URL в миллисекундах.
    /// Используется как верхняя граница backoff, рассчитанного от `RetryBaseDelayMs`.
    /// Ограничивает, насколько далеко retry может быть отложен после нескольких неудачных попыток.
    /// </summary>
    public int RetryMaxDelayMs { get; set; } = 30_000;

    /// <summary>
    /// Интервал запуска reaper-а в секундах для возврата элементов с истекшим lease.
    /// Используется в цикле обработки очереди, чтобы восстанавливать URL, зависшие за умершим worker-ом.
    /// Меньшее значение быстрее освобождает зависшие элементы, но чаще обращается к базе.
    /// </summary>
    public int ReaperIntervalSeconds { get; set; } = 15;
}
